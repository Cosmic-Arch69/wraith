// YAML config generator for the Wraith launch wizard
// Produces valid YAML matching WraithV3Config schema

export interface WizardHost {
  ip: string
  name: string
  webUrl?: string
  webApp?: string
}

export interface WizardState {
  // Engagement
  engagementType: 'external' | 'internal' | 'assumed-breach'
  wanIp: string
  testerName: string
  testerRole: string
  organization: string
  infrastructure: string
  environment: string
  purpose: string
  monitoring: string

  // Target
  domain: string
  dc: string
  hosts: WizardHost[]
  domainUser: string
  domainPass: string

  // Planning
  maxRounds: number
  maxAgents: number
  concurrency: number
  objective: string

  // Attack
  randomize: boolean
  delayMin: number
  delayMax: number

  // Output
  logDir: string
  generateReport: boolean
  skipPreflight: boolean
}

