export function validateConfig(state: WizardState): string[] {
  const errors: string[] = []

  if (state.engagementType === 'external' && !state.wanIp.trim()) {
    errors.push('WAN IP is required for external engagements')
  }
  if (!state.testerName.trim()) errors.push('Tester name is required')
  if (!state.infrastructure.trim()) errors.push('Infrastructure description is required')
  if (!state.domain.trim() && state.engagementType !== 'external') {
    errors.push('Domain name is required for internal/assumed-breach')
  }
  if (state.hosts.length === 0 && state.engagementType !== 'external') {
    errors.push('At least one target host is required')
  }
  if (state.maxRounds < 1 || state.maxRounds > 50) {
    errors.push('Max rounds must be between 1 and 50')
  }
  if (state.maxAgents < 1 || state.maxAgents > 200) {
    errors.push('Max agents must be between 1 and 200')
  }

  return errors
}

export function generateConfigYaml(state: WizardState): string {
  const hostsYaml = state.hosts
    .map((h) => {
      let entry = `    - ip: "${h.ip}"\n      name: "${h.name}"`
      if (h.webUrl) entry += `\n      web_url: "${h.webUrl}"`
      if (h.webApp) entry += `\n      web_app: "${h.webApp}"`
      return entry
    })
    .join('\n')

  return `# Wraith v3.8.0 Configuration
# Generated by Wraith Console

engagement:
  type: ${state.engagementType}
  wan_ip: "${state.wanIp}"
  network_interface: "eth0"
  authorization:
    tester_name: "${state.testerName}"
    tester_role: "${state.testerRole}"
    organization: "${state.organization}"
    infrastructure: "${state.infrastructure}"
    environment: "${state.environment}"
    purpose: "${state.purpose}"
    monitoring: "${state.monitoring}"

target:
  domain: "${state.domain}"
  dc: "${state.dc}"
  hosts:
${hostsYaml}
  credentials:
    domain_user: "${state.domainUser}"
    domain_pass: "${state.domainPass}"

attack:
  randomize: ${state.randomize}
  delay_min_sec: ${state.delayMin}
  delay_max_sec: ${state.delayMax}

planning:
  max_rounds: ${state.maxRounds}
  max_total_agents: ${state.maxAgents}
  max_concurrent_agents: ${state.concurrency}
  objective: ${state.objective}

output:
  log_dir: "${state.logDir}"
  report: ${state.generateReport}

report:
  react_max_iterations: 3
  react_min_tool_calls: 2
`
}
